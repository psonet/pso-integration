/// 32-byte secret key for Hardhat index `idx`. Panics if `idx >= 10`.
pub fn signer_key(idx: usize) -> [u8; 32] {
    HARDHAT_KEYS[idx]
}

/// EVM address derived from [`HARDHAT_KEYS`] at the given index.
/// Computed via `PrivateKeySigner::from_slice(...).address()` so we
/// don't hard-code the addresses (and risk skew with the key table).
pub fn signer_address(idx: usize) -> Address {
    PrivateKeySigner::from_slice(&HARDHAT_KEYS[idx])
        .expect("Hardhat key is a valid secp256k1 scalar")
        .address()
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Sanity check: indices 0 and 1 are the ones pso-chain's `--dev`
    /// genesis pre-funds as registry admin / primary SRA. If either
    /// of these addresses drifts, the SRA registration bootstrap
    /// fails silently against the live node — pin them here.
    #[test]
    fn canonical_first_two_addresses() {
        assert_eq!(
            format!("{:?}", signer_address(0)).to_lowercase(),
            "0xf39fd6e51aad88f6f4ce6ab8827279cfffb92266"
        );
        assert_eq!(
            format!("{:?}", signer_address(1)).to_lowercase(),
            "0x70997970c51812dc3a010c7d01b50e0d17dc79c8"
        );
    }
}
